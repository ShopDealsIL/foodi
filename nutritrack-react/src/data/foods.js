export const foodIndex = FOOD_DB.map(f => ({
  name: f[0], emoji: f[1], cal100: f[2], prot100: f[3],
  carb100: f[4], fat100: f[5], unit: f[6], unitGrams: f[7],
  searchKey: f[0].toLowerCase(),
}));

export function searchFoods(query, limit = 8) {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  // Exact start match first, then includes
  const starts = foodIndex.filter(f => f.searchKey.startsWith(q));
  const includes = foodIndex.filter(f => !f.searchKey.startsWith(q) && f.searchKey.includes(q));
  return [...starts, ...includes].slice(0, limit);
}

export function calcMacros(food, qty) {
  const mult = food.unit === 'item'
    ? (qty * food.unitGrams) / 100
    : qty / 100;
  return {
    calories: Math.round(food.cal100  * mult),
    protein:  Math.round(food.prot100 * mult * 10) / 10,
    carbs:    Math.round(food.carb100 * mult * 10) / 10,
    fat:      Math.round(food.fat100  * mult * 10) / 10,
  };
}
